package com.akshay.gbuattendance.Adapters;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentStatePagerAdapter;

import java.util.ArrayList;

/**
 * Created by Akshay Kumar Kumar on 15/2/16.
 */
public class StartScreenAdapter extends FragmentStatePagerAdapter {
    private ArrayList<Fragment> fragments;

    public StartScreenAdapter(FragmentManager fm, ArrayList<Fragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }
}
